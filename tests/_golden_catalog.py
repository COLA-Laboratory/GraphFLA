"""Frozen catalog of hand-crafted golden landscapes for the independent test bed.

PURE DATA — no graphfla import. Each entry is a small, COMPLETE genotype space
(every combination of position values present, <=50 nodes) with a fitness map
crafted to realise specific landscape properties. Ground-truth feature values
are computed INDEPENDENTLY of graphfla (see _golden_references.py) and asserted
in test_golden_landscapes.py.

Entry schema:
  id            : short unique slug
  kind          : 'boolean' | 'ordinal' | 'categorical' | 'mixed' | 'dna' | 'rna' | 'protein'
  n_positions   : number of variables
  position_types: per-position neighbour rule: 'boolean' (flip), 'ordinal' (+/-1 step),
                  or 'categorical' (any other value). Sequence kinds use 'categorical'
                  over the given alphabet (substitution).
  alphabet      : letters for sequence kinds (else []); genotype ints index into it
  maximize      : optimisation direction
  epsilon       : neutrality threshold used at build time (0 unless neutral plateaus intended)
  genotypes     : list of integer-coded configurations (the full Cartesian product)
  fitness       : fitness per genotype, aligned by index
  properties    : intended/descriptive properties (true structure comes from the references)
  rationale     : how the fitness realises the properties

Generated by curating the output of the Phase-1 generator agents (blind to graphfla),
then validated: all 30 are complete spaces with no ambiguous (0, 0.05) fitness gaps,
and all build in graphfla with the expected node counts.
"""

